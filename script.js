  map.on("popupopen", function () {
    const btn = document.querySelector(".routing-button");
    if (btn) {
      btn.addEventListener("click", function (e) {
        const lat = e.target.dataset.lat;
        const lng = e.target.dataset.lng;
        const destinationLatLng = L.latLng(lat, lng);

        map
          .locate({ setView: false, maxZoom: 16 })
          .on("locationfound", function (e) {
            if (routingControl) {
              map.removeControl(routingControl);
            }

            routingControl = L.Routing.control({
              waypoints: [
                e.latlng, // Titik Awal: Lokasi Pengguna
                destinationLatLng, // Titik Akhir: Lokasi Sekolah
              ],
              routeWhileDragging: false,
              createMarker: function () {
                return null;
              }, // Sembunyikan ikon A dan B
            }).addTo(map);

            map.closePopup();
          })
          .on("locationerror", function (e) {
            alert(
              "Gagal mendapatkan lokasi Anda. Pastikan izin lokasi (GPS) sudah diaktifkan di browser Anda."
            );
          });
      });
    }
  });
