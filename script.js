  document
    .querySelector("#search-widget button")
    .addEventListener("click", () => {
      const query = document
        .querySelector("#search-widget input")
        .value.toLowerCase();
      if (!query) return;

      let found = false;
      for (const layerName in overlayLayers) {
        overlayLayers[layerName].eachLayer((layer) => {
          if (found) return;
          for (const key in layer.feature.properties) {
            const value = String(layer.feature.properties[key]).toLowerCase();
            if (value.includes(query)) {
              const center = layer.getLatLng
                ? layer.getLatLng()
                : layer.getBounds().getCenter();
              map.setView(center, 17);
              setTimeout(() => {
                layer.openPopup();
              }, 300);

              found = true;
              return;
            }
          }
        });
        if (found) break;
      }

      if (!found) {
        alert("Lokasi atau data tidak ditemukan.");
      }
    });
