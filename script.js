  // --- FUNGSI BARU: STYLE TOPOGRAFI ---
  // Fungsi ini memberikan warna berdasarkan properti "Gridcode".
  function styleTopografi(feature) {
    let color;
    // [PERBAIKAN] Menggunakan "gridcode" (huruf kecil) sesuai dengan data JSON
    switch (feature.properties.gridcode) {
      case 1:
        color = "#ff1004ff";
        break; // Merah untuk Tidak Aman
      case 2:
        color = "#ffff00ff";
        break; // Kuning untuk Kurang Aman
      case 3:
        color = "#009541ff";
        break; // Hijau untuk Aman
      default:
        color = "#cccccc"; // Abu-abu jika tidak ada keterangan
    }
    return {
      fillColor: color,
      weight: 0, // Garis batas dihilangkan agar terlihat menyatu
      opacity: 1,
      fillOpacity: 1.0, // Opasitas/transparansi 60%
    };
  }

